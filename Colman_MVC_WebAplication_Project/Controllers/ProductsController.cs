using System;
using System.Collections.Generic;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Colman_MVC_WebAplication_Project.Data;
using Colman_MVC_WebAplication_Project.Models;

namespace Colman_MVC_WebAplication_Project.Controllers
{
    public class ProductsController : Controller
    {
        private StoreDataBase db = new StoreDataBase();

        private bool checkIfAdmin()
        {
            if (Session["user"] != null)
            {
                string userName = Session["user"] as string;
                User user = db.Users.FirstOrDefault(u => u.UserName == userName);
                if (user.isEditor)
                    return true;
            }

            return false;
        }

        // GET: Products
        public ActionResult Index()
        {
            if(!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            var products = db.Products.Include(p => p.Category);
            return View(products.ToList());
        }

        // GET: Products/Details/5
        public ActionResult Details(int? id)
        {
            if (!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }

            Product product = db.Products.Find(id);
            if (product == null)
            {
                return HttpNotFound();
            }
            return View(product);
        }

        // GET: Products/Create
        public ActionResult Create()
        {
            if (!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            ViewBag.CategoryID = new SelectList(db.Categories, "CategoryID", "CategoryName");
            return View();
        }

        // POST: Products/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "ProductID,ProductName,ProductImagePath,Gender,ProductDescription,ProductPrice,ProductOrderCount,CategoryID")] Product product)
        {
            if (ModelState.IsValid)
            {
                db.Products.Add(product);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            ViewBag.CategoryID = new SelectList(db.Categories, "CategoryID", "CategoryName", product.CategoryID);
            return View(product);
        }

        // GET: Products/Edit/5
        public ActionResult Edit(int? id)
        {
            if (!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Product product = db.Products.Find(id);
            if (product == null)
            {
                return HttpNotFound();
            }
            ViewBag.CategoryID = new SelectList(db.Categories, "CategoryID", "CategoryName", product.CategoryID);
            return View(product);
        }

        // POST: Products/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "ProductID,ProductName,ProductImagePath,Gender,ProductDescription,ProductPrice,ProductOrderCount,CategoryID")] Product product)
        {
            if (ModelState.IsValid)
            {
                db.Entry(product).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            ViewBag.CategoryID = new SelectList(db.Categories, "CategoryID", "CategoryName", product.CategoryID);
            return View(product);
        }

        // GET: Products/Delete/5
        public ActionResult Delete(int? id)
        {
            if (!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Product product = db.Products.Find(id);
            if (product == null)
            {
                return HttpNotFound();
            }
            return View(product);
        }

        // POST: Products/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Product product = db.Products.Find(id);
            db.Products.Remove(product);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        public ActionResult SimpleSearch(string productName)
        {
            if (!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            Product curr = db.Products.FirstOrDefault(product => product.ProductName == productName);
            return curr == null ? RedirectToAction("Index") : RedirectToAction("Details", new { id = curr.ProductID });
        }

        public ActionResult Search(int categoryid = 0, double price = double.MaxValue, string gender = "none")
        {
            if (!this.checkIfAdmin())
                return RedirectToAction("index", "Home");

            var products = db.Products.Include(p => p.Category);

            List<Product> searchValues = new List<Product>();
            foreach (Product product in products.ToList())
                if (product.CategoryID == categoryid || categoryid == 0)
                    if (product.ProductPrice <= price)
                        if (product.Gender == gender || product.Gender == "both" || gender == "none")
                            searchValues.Add(product);

            return View(searchValues);
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
